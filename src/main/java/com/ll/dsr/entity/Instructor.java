package com.ll.dsr.entity;


public class Instructor {

  private long coachNumber;
  private long administratorNumber;
  private String idNumber;
  private String coachName;
  private String contact;
  private String region;
  private String yourDrivingSchool;
  private String coachGender;
  private long coachAge;

  private String password;

  public String getPassword() {
    return password;
  }

  public void setPassword(String password) {
    this.password = password;
  }

  public long getCoachNumber() {
    return coachNumber;
  }

  public void setCoachNumber(long coachNumber) {
    this.coachNumber = coachNumber;
  }


  public long getAdministratorNumber() {
    return administratorNumber;
  }

  public void setAdministratorNumber(long administratorNumber) {
    this.administratorNumber = administratorNumber;
  }


  public String getIdNumber() {
    return idNumber;
  }

  public void setIdNumber(String idNumber) {
    this.idNumber = idNumber;
  }


  public String getCoachName() {
    return coachName;
  }

  public void setCoachName(String coachName) {
    this.coachName = coachName;
  }


  public String getContact() {
    return contact;
  }

  public void setContact(String contact) {
    this.contact = contact;
  }


  public String getRegion() {
    return region;
  }

  public void setRegion(String region) {
    this.region = region;
  }


  public String getYourDrivingSchool() {
    return yourDrivingSchool;
  }

  public void setYourDrivingSchool(String yourDrivingSchool) {
    this.yourDrivingSchool = yourDrivingSchool;
  }


  public String getCoachGender() {
    return coachGender;
  }

  public void setCoachGender(String coachGender) {
    this.coachGender = coachGender;
  }


  public long getCoachAge() {
    return coachAge;
  }

  public void setCoachAge(long coachAge) {
    this.coachAge = coachAge;
  }

}
